package com.example.recyclerview;

public class Gtr {

    private String name, year;
    private int image;

    public Gtr(String name, String year, int image) {
        this.name = name;
        this.year = year;
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public String getYear() {
        return year;
    }

    public int getImage() {
        return image;
    }


}
